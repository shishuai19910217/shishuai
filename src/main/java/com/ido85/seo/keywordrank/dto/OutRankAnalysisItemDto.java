package com.ido85.seo.keywordrank.dto;

public class OutRankAnalysisItemDto {
	private String id;
	private String isCom;
	private String name;
	private String ranking;
	private String rankingpromotion;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getIsCom() {
		return isCom;
	}
	public void setIsCom(String isCom) {
		this.isCom = isCom;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getRanking() {
		return ranking;
	}
	public void setRanking(String ranking) {
		this.ranking = ranking;
	}
	public String getRankingpromotion() {
		return rankingpromotion;
	}
	public void setRankingpromotion(String rankingpromotion) {
		this.rankingpromotion = rankingpromotion;
	}
	
}
